  skipLink: { fontSize: fontSize.sm, color: colors.gray400, textDecorationLine: 'underline' },
  uploadBox: { borderWidth: 2, borderColor: colors.gray200, borderStyle: 'dashed', borderRadius: radius.md, alignItems: 'center', justifyContent: 'center', paddingVertical: 40, backgroundColor: colors.gray50, marginBottom: spacing.xl, gap: 6 },
  uploadBoxFilled: { borderColor: colors.green, backgroundColor: colors.greenLight, borderStyle: 'solid' },
