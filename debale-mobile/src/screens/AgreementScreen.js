  progressDotActive: { backgroundColor: colors.teal },
  lockBanner: { flexDirection: 'row', gap: 12, backgroundColor: colors.goldLight, padding: spacing.lg, borderRadius: radius.md, marginBottom: spacing.xl, alignItems: 'flex-start' },
  lockText: { flex: 1, fontSize: fontSize.sm, color: colors.gray700, lineHeight: 19 },
  sectionLabel: { fontSize: fontSize.sm, fontWeight: '600', color: colors.gray700, marginBottom: spacing.md },
  gatewayGrid: { flexDirection: 'row', flexWrap: 'wrap', gap: spacing.sm, marginBottom: spacing.xl },
  gatewayCard: { flexBasis: '47%', flexGrow: 1, flexDirection: 'row', alignItems: 'center', gap: 10, backgroundColor: 'white', borderRadius: radius.md, borderWidth: 2, borderColor: colors.gray200, padding: spacing.md },
  gatewayIcon: { width: 32, height: 32, borderRadius: 8, alignItems: 'center', justifyContent: 'center' },
  gatewayIconText: { color: 'white', fontSize: 9, fontWeight: '700' },
  gatewayName: { fontSize: fontSize.sm, fontWeight: '600', color: colors.dark },
  payBtn: { flexDirection: 'row', gap: 8, backgroundColor: colors.teal, height: 50, borderRadius: radius.md, alignItems: 'center', justifyContent: 'center' },
  payBtnText: { color: 'white', fontWeight: '700', fontSize: fontSize.base },
  successIconWrap: { width: 70, height: 70, borderRadius: 35, backgroundColor: colors.greenLight, alignItems: 'center', justifyContent: 'center', alignSelf: 'center', marginBottom: spacing.lg },
